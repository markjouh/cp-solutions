void solve() {
    int N;
    string S;
    cin >> N >> S;
    int cur = 1, ans = 1;
    for (int i = 1; i < N; i++) {
        if (S[i] == S[i - 1]) {
            cur++;
        } else {
            cur = 1;
        }
        ans = max(ans, cur);
    }
    cout << ans + 1 << '\n';
}
 
